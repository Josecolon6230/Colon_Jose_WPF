//round()
//Normal rounding .5 goes up, less goes down

var num1 = 9.444;
console.log(num1);
console.log(Math.round(num1));


//Math.floor()
//floor chops off all decimals and always rounds down.
var num2 = 6.9;
console.log(num2);
console.log(Math.floor(num2));


//Math.ceil()
//Always rounds up


var num3 = 4.8;
console.log(num3);
console.log(Math.ceil(num3));